"""Unit tests for app/config.py — Settings and LLMSemaphoreLimits."""
import os
from unittest.mock import patch

import pytest
from pydantic import ValidationError

from app.config import LLMSemaphoreLimits, Settings


class TestLLMSemaphoreLimits:
    def test_default_interactive(self):
        assert LLMSemaphoreLimits().interactive == 5

    def test_default_background(self):
        assert LLMSemaphoreLimits().background == 2

    def test_default_audit(self):
        assert LLMSemaphoreLimits().audit == 1

    def test_custom_values(self):
        lim = LLMSemaphoreLimits(interactive=10, background=4, audit=2)
        assert lim.interactive == 10
        assert lim.background == 4
        assert lim.audit == 2

    def test_non_integer_raises(self):
        with pytest.raises(ValidationError):
            LLMSemaphoreLimits(interactive="not-a-number")


